use game2d::{graphics::{graphics::{Drawable, Draw}, images::Image}, game::common::{Dimension, Position}};

pub type MAP_LEVEL = Vec<Vec<i32>>;

pub struct MAP(MAP_LEVEL);

impl Draw for MAP {
    fn draw(&mut self, graphics: &mut game2d::graphics::graphics::Graphics) {


        for (pos_l, l) in self.0.iter().enumerate()
        {
            for (pos_c, c) in l.iter().enumerate() {
                let image;
                match c {
                    1 => {
                        image = graphics.new_image("images/tile1.png");
                    },
                    _ => { image = Err("Error".to_string()); },
                }
                
                if let Ok(image) = image {
                    graphics.draw(
                        &image, 
                        (pos_c as Dimension * image.get_width()) as Position, 
                        (pos_l as Dimension * image.get_height()) as Position, 
                        0., 1., 1., 0., 0. 
                    );
                }
            }
        }        
    }
}

impl MAP {

    pub fn load_level(&mut self, level: i32) {
        match level {
           1 => { self.0 = level_1(); }
           _ => { self.0 = Vec::new(); }
        };
    }
}

impl Default for MAP {
    fn default() -> Self {
        MAP(Vec::new())
    }
}

